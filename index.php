                Red Dead Redemption 2 Special Edition
                La edición especial contiene los siguientes contenidos para el modo historia:        
                Misión de Atraco a banco y Asalto a guarida
                Pura sangre tordo negro
                Bonificaciones jugables de Talismán y Medallón
                Potenciadores, bonificaciones de dinero y descuentos
                Atuendo de pistolero de Nuevo Paraíso
