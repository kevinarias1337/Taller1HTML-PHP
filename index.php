        <p>
                Red Dead Redemption 2 es la secuela del videojuego de éxito de Rockstar Red Dead Redemption (2010),
                considerado como uno de los mejores títulos de la compañía. Se trata de la tercera entrega de la saga Red Dead,
                iniciada en 2004 con Red Dead Revolver y nos trasladará de nuevo al Salvaje Oeste para ponernos en el escenario
                de mundo abierto más grande jamás creado por los padres de Grand Theft Auto y contarnos una historia sobre forajidos, 
                venganza y atracos en un escenario de gran realismo en PlayStation 4 y Xbox One.
        </p>
        <p>
                Red Dead Redemption 2 se trata de un western crepuscular. Nos lleva al comienzo del fin del Salvaje Oeste,
                cuando las bandas de forajidos sufren el acoso de la ley y los últimos grandes ladrones comienzan a rendirse, 
                sucumbir al avance de la civilización o resistirse siendo asesinados. El videojuego nos emplazará justo después 
                de un desastroso atraco fallido en la ciudad de Blackwater, tras el que nuestro protagonista Arthur Morgan y la banda
                de de Van der Linde se verán obligados a huir. Perseguidos por agentes federales, la banda de Van der Linde se 
                encontrará en una constante huida hacia adelante, en la que tendrán que atracar, robar y luchar para sobrevivir en
                su camino por el escabroso territorio de la América del 1899.
                <br>
                El argumento del videojuego nos llevará a entablar relación con los integrantes de este grupo de delincuentes y errantes,
                invitándonos a elegir entre nuestros propios ideales y la lealtad a la banda que lo vio crecer y la cual le concedió una 
                oportunidad. Esta banda estará formada por algunos personajes muy conocidos del universo Red Dead, como John Marston,
                Bill Williamson, Javier Escuella, Sadie Adler, Charles Smith, Micah Bell y Hosea Matthews. Sus conversaciones,
                intereses y trasfondos, marcan y arman el guion del título.
        </p>
