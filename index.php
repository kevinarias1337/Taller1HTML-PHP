                Acceso al tema de personalización de Superviviente para el campamento
                Acceso a las armas pistola Volcanic, escopeta de corredera y fusil Lancaster Varmint en las armerías del modo historia
                Bonificaciones de nivel
                Además del contenido in-game, la edición trae un mapa físico y un estuche metálico exclusivo.       
                Collector's Box
                Red Dead Redemption II Collectors Box
                Se trata de una caja de coleccionista, enfocada en proporcionar objetos de la temática de la saga. No es una edición especial de Red Dead Redemption 2, ya que no inlcuye dicho videojuego ni los contenidos para modo historia y/o multijugador de las ediciones Special y Ultimate. Incluye los siguientes contenidos:       
                Caja de diezmos metálica con cerradura y llave
                Moneda coleccionable de desafío
                Puzle de doble cara
                Bandana Six Shooter
                Mapa del Tesoro
                Set de pins
