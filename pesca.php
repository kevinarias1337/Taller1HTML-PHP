            En el mundo hay muchos lugares en los que podrás pescar, algo que podrás detectar si te fijas en el agua
            y ves unas pequeñas hondas que hacen los peces. Equípate con tu caña de pescar en el dial de objetos y elige 
            un cebo apropiado para la especie que intentas capturar.
