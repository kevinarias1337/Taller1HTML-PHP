        <p>
                <b>L1/LB:</b> mantén para mostrar el dial de pesca.</br>
                <b>Triángulo/Y:</b> después de atrapar un pez, pulsa para equipar un cebo rápidamente.</br>
                <b>L2/LT:</b> mantén para sujetar la caña.</br>
                <b>R2/RT:</b> manteniendo <b>L2/LT</b>, mantén y suelta para lanzar el sedal; pulsa para atrapar un pez; pulsa para mover el cebo.</br>
                <b>Stick izquierdo:</b> manteniendo <b>L2/LT</b>, mover la caña.</br>
                <b>Stick derecho:</b> rotan en el sentido de la agujas del reloj para recoger el sedal.</br>
                <b>Círculo/B:</b> volver a lanzar; cortar el sedal.</br>              
        </p>
