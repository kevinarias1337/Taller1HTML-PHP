        <p>
            Los cebos de comida (como el pan) y los naturales (como los gusanos) son baratos y fáciles de conseguir,
            pero se pierden cuando pica un pez, tanto si lo atrapas como si se escapa. Esto significa que tendrás que volver 
            a poner un cebo en el anzuelo.
            Los señuelos artificiales son más caros que los cebos, pero pueden usarse más de una vez. 
            Hay tres tipos distintos de señuelos, diseños para atraer a peces de río, lago o pantano. Tras lanzar 
            el sedal usando un señuelo, recógelo lentamente para que parezca un insecto o un pez pequeño que está nadando.
            Un señuelo inmóvil no atraerá a pez alguno. Esto hace que la pesca con señuelo sea mucho más difícil, pero es 
            la única manera de atraer a los ejemplares más grandes.
        </p>
