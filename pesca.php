        <p>
            Mantén L2/LT para sujetar la caña con firmeza, y luego mantén y suelta R2/RT para lanzar el sedal. 
            Cuando piquen con firmeza los peces tirarán con fuerza de la caña de pescar hacia abajo, momento en el que deberás pulsar R2/RT
            para intentar enganchar al pez antes de que se vaya.
            Cada especie de pez muerde el cebo de forma distinta. Algunas lo mordisquean con cautela antes de darle un buen bocado,
            mientras que otras se muestran más confiadas y muerden con firmeza repetidas veces. Las especies depredadoras pueden 
            morder de repente sin avisar, así que debes estar preparado en todo momento para actuar. Cuando un pez muerda con firmeza
            el cebo, el corcho se sumergirá: será el momento de actuar.               
            Cuando un pez haya picada se está debatiendo, intentará escapar de la manera más agresiva. 
            El objetivo es controlarlo tirando de la caña hacia la izquierda o la derecha, en la dirección contraria al movimiento del pez.
            Esto hará que la posible captura se vaya cansando y oponga menos resistencia. Cuando veas que ocurre esto,
            aprovecha la oportunidad para recoger tanto sedal como puedas antes de que vuelva a oponer resistencia.               
            Tras recoger el sedal y enganchar al pez, podrás quedártelo para comerlo o venderlo. Si está vivo, 
            también podrás devolverlo al agua para ganar honor. Los peces pequeños se guardarán en el zurrón, 
            mientras que los de mayor tamaño se cargarán a lomos del caballo.                              
        </p>
