        <p>
            La gran mayoría de los animales son muy escurridizos y asustadizos, y se alertan rápidamente ante nuestra presencia,
            aunque estemos a decenas de metros, por lo que es importante moverse con sigilo y caminar agachados, pulsando el stick
            izquierdo.
            Esto evidentemente no es suficiente, y cuando sospechemos que hay animales en los alrededores,
            algo que ocurre en cualquier escenario natural del juego, lo mejor que podemos hacer es rastrearlos, 
            activando el Eagle Eye pulsando los dos sticks del mando a la vez.
            En este modo de visión las huellas de los animales se identifican claramente mediante rastros luminosos, 
            y si pulsas R1/RB puede resaltarlos durante un rato, incluso después de haber abandonado el Eagle, 
            lo cual te facilitará seguirlos.
            Si durante el Eagle Eye te fijas, podrás ver alrededor del cuerpo de tu personaje un efecto visual muy sutil, 
            que representa el olor del personaje, que es transportado por el viento, y que permite a los animales olerte a 
            decenas de metros y así salir huyendo; utiliza esta información para saber si la brisa llevará tu olor hacia la presa.
            No solo te puedes guiar con el Eagle Eye para detectar la presencia de animales, y hay pistas y señales un poco 
            más sutiles en la naturaleza, como huellas en el suelo, excrementos o restos de presas devoradas en el caso de los depredadores.
        </p>
