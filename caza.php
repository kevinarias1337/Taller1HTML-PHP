        <p>
            Además de las armas y nuestros instintos podemos echar manos de diversos objetos que nos ayudarán en la caza,
            que son los siguientes:
            Los cebos atraen a los animales salvajes, aunque hay varios tipos como cebos para depredadores o herbívoros,
            así que tienes que usar el adecuado en cada ocasión. Deja uno en el suelo y escóndete cerca para sorprenderle cuando llegue,
            aunque en ocasiones pueden tardar minutos en aparecer. También puedes usar los animales recién muertos para atraer a ciertos 
            carnívoros.
            Los inhibidores de olores sirven para que a los animales les cuesta más olerte y así detectar tu presencia, por lo que si usas
            uno te podrás acercar más fácilmente a ellos usando el sigilo.
