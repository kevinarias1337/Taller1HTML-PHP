        <p>
            Después de cazar los animales puedes despellejar a la mayoría, obteniendo una serie de materiales. Las pieles y 
            las partes de animales se guardan en el zurrón o se cargan en el caballo, acercándote a la parte trasera y pulsando cuadrado/X.
            Cuando descanses en una hoguera podrás cocinar y comer los distintos tipos de carne que hayas obtenido cazando. 
            Comer es una de las maneras más apropiadas para recuperar los núcleos de salid y resistencia. Ten en en cuenta que 
            la comida que cocines la puedes comer en el momento o guardarla para más tarde.
            También puedes llevar las partes de los animales al campamento y donarlas a la banda, o vendérselas a los carniceros 
            de las ciudades y así conseguir dinero. En cualquiera de los dos casos haz algo con las piezas de caza ya que pasado 
            un tiempo empezarán a descomponerse y perderán todo su valor.
            Algunas partes de los animales pueden venderse en las tiendas y en los comercios de los tramperos, o puedes donarlas
            en el campamento a Pearson para desbloquear mejoras. En nuestra sección de la guía Todos sobre los tramperos y objetos
            de creación te contamos todos los objetos que puedes llegar a fabricar con las pieles de los animales.
            Otras partes de los animales, como las garras o los dientes, pueden utilizarse para crear talismanes en los peristas.
            Ten en cuenta además que despellejando animales desbloquearás niveles de desafíos de Maestro de cazador, algo que te 
            explicamos en la sección de la guía Todos los Desafíos de Red Dead Redemption 2 y cómo conseguirlos.
        </p>
