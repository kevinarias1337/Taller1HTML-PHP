            GUIA DE CAZA, TRUCOS Y CONSEJOS.
        </h1>
        <p>
            La caza es una de las actividades principales que podemos realizar en el bello y enorme mundo de Red Dead Redemption 2,
            con más de 200 especies distintas de la que podemos conseguir importantes recursos, como carne, pieles y otros materiales
            útiles, que pueden servir para alimentarnos, para vender y obtener dinero o incluso para fabricarnos nuevo equipo.
            Atención vamos a contaros todo lo que tenéis que saber para convertiros en grandes cazadores.
        </p>
