        <p>
            A la hora de cazar tienes que elegir tu equipo con cuidado para no dañar las pieles y otros materiales de los animales.
            Cuanto más dañadas estén las pieles por ejemplo por disparos menos valor tendrán a la hora de venderlas.
            Al menos en los primeros compases de la aventura el arco es la mejor herramienta para cazar, 
            ya que te permite matar limpiamente y en silencio, sin asustar a los animales que estén a los alrededores. 
            Intenta siempre apuntar a la cabeza o al cuello de la presa, que son las partes más sensibles de los animales.
            Ten en cuenta que puedes llamar a un animal mientras lo apuntas. Si lo haces justo antes de soltar la 
            flecha puede ayudarte a darle en la cabeza, ya que el animal al escucharte la levantará mirando hacia ti.
            Esto se realizar con el mismo botón de silbar cuando estás apuntando con el arco, pulsando arriba en el mando.
            Hay varios tipos de flechas especiales que resultan muy útiles a la hora de cazar, como las flechas para presas
            pequeñas que te permiten obtener pieles de la máxima calidad, o las envenenadas, ideales para acabar con grandes 
            animales rápidamente. Ambas se pueden comprar o fabricar.
        </p>
